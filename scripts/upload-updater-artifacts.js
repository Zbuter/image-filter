import { execSync } from 'child_process';
import fs from 'fs';
import path from 'path';

const tag = process.env.RELEASE_TAG || '';
const repo = process.env.RELEASE_REPO || '';

if (!tag || !repo) {
  console.error('Missing RELEASE_TAG or RELEASE_REPO env vars');
  process.exit(1);
}

function findFilesRecursive(dir) {
  let results = [];
  try {
    const entries = fs.readdirSync(dir, { withFileTypes: true });
    for (const entry of entries) {
      const fullPath = path.join(dir, entry.name);
      if (entry.isDirectory()) {
        results = results.concat(findFilesRecursive(fullPath));
      } else {
        results.push(fullPath);
      }
    }
  } catch (e) {}
  return results;
}

// Search all bundle directories
const targetDir = path.join('src-tauri', 'target');
let allBundleFiles = [];

for (const subPath of ['release', 'universal-apple-darwin/release']) {
  const bundleDir = path.join(targetDir, subPath, 'bundle');
  if (fs.existsSync(bundleDir)) {
    console.log('Scanning:', bundleDir);
    allBundleFiles = allBundleFiles.concat(findFilesRecursive(bundleDir));
  }
}

console.log('All bundle files found:', allBundleFiles.length);
allBundleFiles.forEach(f => console.log('  ', f));

// Collect files to upload
let filesToUpload = [];

// macOS: upload .tar.gz and .tar.gz.sig directly
const macTar = allBundleFiles.find(f => f.endsWith('.app.tar.gz'));
const macTarSig = allBundleFiles.find(f => f.endsWith('.app.tar.gz.sig'));
if (macTar) filesToUpload.push(macTar);
if (macTarSig) filesToUpload.push(macTarSig);

// Windows: need to create .nsis.zip from the NSIS installer
const nsisExe = allBundleFiles.find(f => f.includes('-setup.exe') && !f.endsWith('.sig'));
const nsisSig = allBundleFiles.find(f => f.includes('-setup.exe.sig'));
const msiFile = allBundleFiles.find(f => f.endsWith('.msi') && !f.endsWith('.sig'));
const msiSig = allBundleFiles.find(f => f.endsWith('.msi.sig'));

// Check if .nsis.zip already exists (from createUpdaterArtifacts)
const existingNsisZip = allBundleFiles.find(f => f.includes('.nsis.zip') && !f.endsWith('.sig'));

if (existingNsisZip) {
  // Already generated by Tauri
  filesToUpload.push(existingNsisZip);
  const existingNsisZipSig = allBundleFiles.find(f => f.includes('.nsis.zip.sig'));
  if (existingNsisZipSig) filesToUpload.push(existingNsisZipSig);
  console.log('Found existing NSIS zip:', existingNsisZip);
} else if (nsisExe) {
  // Create .nsis.zip manually
  const zipName = path.basename(nsisExe) + '.zip';
  const zipPath = path.join(path.dirname(nsisExe), zipName);
  
  console.log('Creating NSIS updater zip:', zipName);
  
  // Use PowerShell on Windows or zip on macOS/Linux to create the archive
  try {
    if (process.platform === 'win32') {
      execSync(`powershell -Command "Compress-Archive -Path '${nsisExe}' -DestinationPath '${zipPath}' -Force"`, { stdio: 'inherit' });
    } else {
      execSync(`cd "${path.dirname(nsisExe)}" && zip -j "${zipPath}" "${path.basename(nsisExe)}"`, { stdio: 'inherit' });
    }
    filesToUpload.push(zipPath);
    console.log('Created:', zipPath);
    
    // Sign the zip file using tauri signer if available
    const privateKey = process.env.TAURI_SIGNING_PRIVATE_KEY;
    if (privateKey) {
      try {
        // Use the tauri CLI to sign the zip
        execSync(`npx tauri signer sign "${zipPath}"`, { 
          stdio: 'inherit',
          env: { ...process.env }
        });
        const zipSigPath = zipPath + '.sig';
        if (fs.existsSync(zipSigPath)) {
          filesToUpload.push(zipSigPath);
          console.log('Signed zip:', zipSigPath);
        }
      } catch (e) {
        console.log('Note: Could not sign zip with tauri signer, trying alternative...');
        // Try using the sig file from the exe (not ideal but better than nothing)
        // Actually, we should skip signing if we can't do it properly
      }
    }
  } catch (e) {
    console.error('Failed to create zip:', e.message);
  }
}

// Also check for .msi.zip
const existingMsiZip = allBundleFiles.find(f => f.includes('.msi.zip') && !f.endsWith('.sig'));
if (existingMsiZip) {
  filesToUpload.push(existingMsiZip);
  const existingMsiZipSig = allBundleFiles.find(f => f.includes('.msi.zip.sig'));
  if (existingMsiZipSig) filesToUpload.push(existingMsiZipSig);
}

console.log('Files to upload:', filesToUpload);

for (const filepath of filesToUpload) {
  const filename = path.basename(filepath);
  try {
    console.log('Uploading:', filename);
    execSync(`gh release upload "${tag}" "${filepath}" --repo "${repo}" --clobber`, {
      stdio: 'inherit'
    });
    console.log('  Success:', filename);
  } catch (e) {
    console.error('  Warning: failed to upload', filename);
  }
}

console.log('Done uploading updater artifacts');
